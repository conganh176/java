package com.company;

public class Ceiling {
    private int length;
    private int paintedColor;

    public Ceiling(int length, int paintedColor) {
        this.length = length;
        this.paintedColor = paintedColor;
    }

    public int getLength() {
        return length;
    }

    public int getPaintedColor() {
        return paintedColor;
    }
}
